
# Working with For Loops

colors = ['red','blue','green','orange','purple']

for i in colors:
    print(i)

print("-----------------------------------")
print('for a in range (10):')

for a in range (10):
    print(a)

print("-----------------------------------")
print('for a in range (10,15):')

for a in range (10,15):
    print(a)

print("-----------------------------------")
print('for a in range (10,30,2):')

for a in range (10,30,2):
    print(a)

